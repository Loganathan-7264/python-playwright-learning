# Printing variables & types
a = 10
b = 34.2
c = "Tony"
d = True
e = [1,2,3]
f = (1,2,3)
g = {1,2,3}
h = {"name":"Loga", "name2":"Sathish"}

print(a, b, c, d, e, f, g, h)
print(type(a), type(b), type(c), type(d), type(e), type(f), type(g), type(h))

# Swapping
x = 10
y = 30
print("Before:", x, y)
x, y = y, x
print("After:", x, y)
